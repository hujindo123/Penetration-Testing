def Encode(msg):
		out=""
		for char in msg:
				if char == ' ':
						print
				else:
					out+=CODE[char.upper()]+' '
		return out
def Decode(msg):
	
	out=''
	sumstr=''
	for char in msg:
		if char == ' ':
			sumstr+=DECODE[out]
			out=''
			continue
		out+=char
	sumstr+=DECODE[out]
	return sumstr
	
